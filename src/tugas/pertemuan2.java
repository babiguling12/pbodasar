package tugas;

public class pertemuan2 {

    public static void main(String args[]) {
        int bilBulat = 1;
        float bilDesimal = 1.5f;
        double bilDesimal2 = 1.67d;
        long bilPanjang = 313421L;
        short bilPendek = 3241;
        char huruf = 'a';
        String kata = "mahasiswa";
        boolean kondisi = true;
        
        System.out.println("========== TIPE DATA ==========");
        System.out.println("integer\t : " + bilBulat);
        System.out.println("float\t : " + bilDesimal);
        System.out.println("double\t : " + bilDesimal2);
        System.out.println("long\t : " + bilPanjang);
        System.out.println("short\t : " + bilPendek);
        System.out.println("char\t : " + huruf);
        System.out.println("String\t : " + kata);
        System.out.println("boolean\t : " + kondisi);
    }
}
